# > OPERAÇÕES MATEMÁTICAS (ARITMÉTICAS)

# - Soma: +
# - Subtração: -
# - Multiplicação: *
# - Divisão: /
# - Divisão inteira: // (descarta a parte decimal)
# - Resto da divisão: % (operador módulo)
# - Potência: **

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 % 3)  # Qual é o resto da divisão de 20 por 3
print(2 ** 3)

# > OPERADORES BOOLEANOS

